    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
         try {
    Connection conn = koneksi.getkoneksi();
    String sql = "SELECT * FROM tb_login WHERE USERNAME = ? AND PASSWORD = ?";
    PreparedStatement pstmt = conn.prepareStatement(sql);
    pstmt.setString(1, txtuser.getText());
    pstmt.setString(2, txtpass.getText());
    ResultSet rs = pstmt.executeQuery();

    if (rs.next()) {
        String bagian = rs.getString("BAGIAN");
        if ("ADMIN".equalsIgnoreCase(bagian)) {
            JOptionPane.showMessageDialog(null, "Selamat Datang Admin", "Success", JOptionPane.INFORMATION_MESSAGE);
            new Admin().show();
            dispose();
        } else if ("KASIR".equalsIgnoreCase(bagian)) {
            JOptionPane.showMessageDialog(null, "Selamat Datang Kasir!", "Success", JOptionPane.INFORMATION_MESSAGE);
            new Kasir().show();
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Bagian tidak dikenal", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(null, "Username atau Password anda Salah!", "", JOptionPane.ERROR_MESSAGE);
        
        txtuser.requestFocus();
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
}


    }//GEN-LAST:event_LoginActionPerformed

