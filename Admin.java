    private void t_produkMouseClicked(java.awt.event.MouseEvent evt) {                                      
        btnsimpan.setEnabled(false);
        btnedit.setEnabled(true);
        btnhapus.setEnabled(true);
        btnbatal.setEnabled(true);
        
        int i = t_produk.getSelectedRow();
        if (i == -1) {
            return;
        }
        String Nomor = (String) model.getValueAt(i, 0);
        String Judul_Buku = (String) model.getValueAt(i, 1);
        String Harga = (String) model.getValueAt(i, 2);
        String Stok = (String) model.getValueAt(i, 3);
        
        
        txNo.setText(Nomor);
        txJudulBuku.setText(Judul_Buku);
        txHarga.setText(Harga);
        txStok.setText(Stok);
    
    }                                    
                       

