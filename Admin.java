    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
     int i = t_produk.getSelectedRow();
        if (i == -1) {
            return;
        }
        
        String Nomor = (String) model.getValueAt(i, 0);
        
        int pernyataan = JOptionPane.showConfirmDialog(null, "Yakin Data Akan Dihapus","Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (pernyataan== JOptionPane.OK_OPTION) {
            try {
                Connection con = koneksi.getkoneksi();
                String sql = "DELETE FROM tb_produk WHERE Nomor = ?";
                PreparedStatement p = con.prepareStatement(sql);
                p.setString(1, Nomor);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            } catch (Exception e) {
                System.out.println("Terjadi Kesalahan");
            }finally{
                btnsimpan.setEnabled(true);
                btnedit.setEnabled(false);
                btnhapus.setEnabled(false);
                btnbatal.setEnabled(false);
                loadData();
                autonumber();
                clear();
            }
        }
        if (pernyataan == JOptionPane.CANCEL_OPTION) {
            
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
    String No = txNo.getText();
    String Judul_Buku = txJudulBuku.getText();
    String Harga = txHarga.getText();
    String Stok = txStok.getText();
    
    try {
        Connection con = koneksi.getkoneksi();
        String sql = "INSERT INTO tb_produk VALUES (?, ?, ?, ?)";
        PreparedStatement p = con.prepareStatement(sql);
        p.setString(1, No);
        p.setString(2, Judul_Buku);
        p.setString(3, Harga); // Ini harusnya placeholder ke-4
        p.setString(4, Stok);          // Ini harusnya placeholder ke-6
        p.executeUpdate();
        p.close();
        JOptionPane.showMessageDialog(null, "Data Tersimpan");
        loadData(); // Pastikan metode ini ada
        
    } catch (Exception e) {
        e.printStackTrace(); // Ini akan mencetak kesalahan ke console
        JOptionPane.showMessageDialog(null, "Terjadi Kesalahan! Harap input dengan benar");
    } finally {
        clear();
        autonumber(); // Pastikan metode ini ada
            // Pastikan metode ini ada
    }                   
                            
    }//GEN-LAST:event_btnsimpanActionPerformed

