     private void autonumber(){
        try {
            Connection con = koneksi.getkoneksi();
            Statement s = con.createStatement();
            String sql = "SELECT * FROM tb_produk ORDER BY Nomor DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoBuku = r.getString("Nomor").substring(2);
                String BR = "" +(Integer.parseInt(NoBuku)+1);
                String Nol = "";
                
                if (BR.length()==1) 
                    {Nol = "00";}
                else if(BR.length()==2)
                    {Nol = "0";}
                else if(BR.length()==3)
                    {Nol = "";}
                
                txNo.setText("PR" + Nol + BR);  
            }else{
                txNo.setText("PR001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
