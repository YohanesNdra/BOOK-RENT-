   public void totalBiaya(){
       int jumlahBaris = tabelbuku.getRowCount();
       int totalBiaya = 0;
       int jumlahProduk, hargaProduk;
       for (int i = 0; i < jumlahBaris; i++){
          jumlahProduk = Integer.parseInt(tabelbuku.getValueAt(i, 2).toString());
          hargaProduk = Integer.parseInt(tabelbuku.getValueAt(i, 4).toString()); 
          totalBiaya  = totalBiaya = totalBiaya + (jumlahProduk * hargaProduk);
        }       
        totalbayar1.setText(String.valueOf(totalBiaya));
        totalbayar2.setText("Rp "+ totalBiaya +",00");
    }

     public void loadData(){
        DefaultTableModel model = (DefaultTableModel) tabelbuku.getModel();
        model.addRow(new Object[]{
          
            fieldidproduk.getText(),
            fieldproduk.getText(),
            fieldjumlah.getText(),
            fieldharga.getText(),
            totalbayar1.getText()
        });
    }

     public void kosong(){
        DefaultTableModel model = (DefaultTableModel) tabelbuku.getModel();
        
        while (model.getRowCount()>0) {
            model.removeRow(0);
        }
    }
