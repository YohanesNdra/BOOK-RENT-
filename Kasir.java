     public void tambahTransaksi(){
        int jumlah, harga, total;
        
        jumlah = Integer.valueOf(fieldjumlah.getText());
        harga = Integer.valueOf(fieldharga.getText());
        total = harga * jumlah;
        
        totalbayar1.setText(String.valueOf(total));
        
        loadData();
        totalBiaya();
        clear2();
        fieldidproduk.requestFocus();
    }

   
